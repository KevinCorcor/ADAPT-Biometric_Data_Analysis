package DataSorting;

public class Peak
{
	double media;
	double begin;
	double end;
	double apex;
	double median;
	
	public Peak()
	{
		median = 0;
		begin = 0;
		end = 0;
		apex = 0;
		median = 0;
	}
	public Peak(double media, double begin, double end, double apex, double median)
	{
		this.median = median;
		this.begin = begin;
		this.end = end;
		this.apex = apex;
		this.median = median;
	}
	
}
